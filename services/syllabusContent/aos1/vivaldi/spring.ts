每首协奏曲都为**独奏小提琴**配以**弦乐队**而作。在协奏曲中，管弦乐队有时可能被称为**全奏**。在乐谱中，独奏声部出现在顶部，然后是两个管弦乐小提琴声部，接着是中提琴（用中音谱号），最后是低音和通奏低音声部。

